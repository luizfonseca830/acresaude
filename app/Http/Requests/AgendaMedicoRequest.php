<?php

namespace App\Http\Requests;

use Illuminate\Foundation\Http\FormRequest;

class AgendaMedicoRequest extends FormRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            //
            'titulo' => 'required|max:100',
            'auxEspecialidade' => 'required',
            'start' => 'required',
            'end' => 'required',
            'intervalo' => 'required|numeric|min:15',
            'descricao' => 'nullable|max:100|'
        ];
    }
    public function messages()
    {
        return [
            'titulo.required' => 'Esse campo precisa ser preenchido',
            'titulo.max' => 'Esse campo tem limite de 100 caracteres',

            'auxEspecialidade.required' => 'O campo especialidade é obrigatório.',

            'start.required' => 'Esse campo precisa ser preenchido',
            'end.required' => 'Esse campo precisa ser preenchido',

            'intervalo.required' => 'O campo intervalo é de preenchimento obrigatório.',
            'intervalo.numeric' => 'O campo intervalo só aceita números.',
            'intervalo.min' => 'O campo intervalo tem que ter no mínimo 15 minutos de diferença entre as consultas.',

            'descricao.max' => 'Esse campo tem limite de 100 caracteres',
        ]; // TODO: Change the autogenerated stub
    }
}
